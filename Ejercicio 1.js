//Crea un programa dónde puedas introducir la edad de dos alumnos y que se evalúe sin van o no los dos a la misma clase.//

const age=prompt("Introduce la edad del primer alumno");
const age2=prompt("Introduce la edad del segundo alumno");;

if (age === age2){
    alert("Van a la misma clase.")
}

else{
    alert("No van a la misma clase.")
}